package cn.com.usrobot.restful.pojo;

public class VariablePresetPositionMap {
    private Integer variableId;

    private Integer inspectionPositionId;

    private Integer ptzPresetNumberId;

    public Integer getVariableId() {
        return variableId;
    }

    public void setVariableId(Integer variableId) {
        this.variableId = variableId;
    }

    public Integer getInspectionPositionId() {
        return inspectionPositionId;
    }

    public void setInspectionPositionId(Integer inspectionPositionId) {
        this.inspectionPositionId = inspectionPositionId;
    }

    public Integer getPtzPresetNumberId() {
        return ptzPresetNumberId;
    }

    public void setPtzPresetNumberId(Integer ptzPresetNumberId) {
        this.ptzPresetNumberId = ptzPresetNumberId;
    }
}